int		ft_intlen(unsigned int n)
{
	unsigned int		i;
	int					count;

	i = n;
	count = 0;
	if (n >= 10)
	{
		while (i >= 10)
		{
			i = i / 10;
			count++;
		}
		return (count + 1);
	}
	return (1);
}
