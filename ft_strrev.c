#include "libft.h"

char		*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	tmp;

	j = ft_strlen(str);
	i = -1;
	while (++i < --j)
	{
		tmp = str[i];
		str[i] = str[j];
		str[j] = tmp;
	}
	return (str);
}
