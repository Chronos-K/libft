#include "libft.h"

void	ft_putnbr(int n)
{
	int		b;
	int		c;

	if (n >= 0)
	{
		if (n >= 10)
		{
			b = n / 10;
			ft_putnbr(b);
		}
		c = n % 10;
		c = c + 48;
		ft_putchar(c);
	}
	if (n < 0 && n != -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	if (n == -2147483648)
	{
		ft_putnbr(-21474836);
		ft_putnbr(48);
	}
}
